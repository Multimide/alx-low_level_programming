/**
 * _isupper- This is a function that checks if a
 * character is uppercased or not
 *
 * @c: arguement to be passed to the function
 *
 * Return: 1 if uppercae or 0 if not
 */

int _isupper(int c)
{
	if (c >= 'A' && c <= 'Z')
	{
		return (0);
	}

	else if (c <= 'z')
	{
		return (0);
	}

	else
	{
		return (0);
	}

}
